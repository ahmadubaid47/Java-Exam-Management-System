    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         if(Qid.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null,"#Question id is Empty");
        }else if(jt1.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null,"Question feild is Empty");
        }
        else if(opt1.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null,"option 1 Field is Empty");
        }
        else if(opt2.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null,"option 2 Field is Empty");
        }else if(opt3.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null,"option 3 Field is Empty");
        }else if(opt4.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null,"option 4 Field is Empty");
        }else if(correctopt.getText().isEmpty()){
            
            JOptionPane.showMessageDialog(null,"Correct option Field is Empty");
        }else{
              try
       {
         
           
          dbt.Question=jt1.getText();
          dbt.qid=Integer.parseInt(Qid.getText());
          dbt.option1=opt1.getText();
          dbt.option2=opt2.getText();
          dbt.option3=opt3.getText();
          dbt.option4=opt4.getText();
          dbt.CorrectOpt=correctopt.getText();
          dbt.Dificulty=Integer.parseInt(Dificulty.getText());
                  
           
       dbt.Catagory= c1.getSelectedItem().toString();
            int res = dao.InsertQuestion(dbt);
            if (res>0)
            {
           
                JOptionPane.showMessageDialog(null,"Saved Successfully");
              
            }
            }
        catch(HeadlessException ex)
        {
            JOptionPane.showMessageDialog(null,"Exception Occured-->"+ex.getMessage());
        }
        }
            
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
if(Qid.getText().isEmpty()){
JOptionPane.showMessageDialog(null,"Your Question id is Empty");
}else{


        try
        {
            ResultSet rs ;
            dbt.qid = Integer.parseInt(Qid.getText());
            rs =dao.SelectbyID(dbt);
            try {
                while(rs.next()){
                    
               
                    jt1.setText(rs.getString(1));
                    c1.getModel().setSelectedItem(rs.getString(2));
                    opt1.setText(rs.getString(4));
                    opt2.setText(rs.getString(5));
                    opt3.setText(rs.getString(6));
                    opt4.setText(rs.getString(7));
                    correctopt.setText(rs.getString(8));
                    
                    
                    break;
                }
            } catch (SQLException ex) {
                
            }
           
          
            
        }
        catch(HeadlessException ex)
        {
            JOptionPane.showMessageDialog(null,"Exception Occured-->"+ex.getMessage());
        }
}
    }//GEN-LAST:event_jButton4ActionPerformed

